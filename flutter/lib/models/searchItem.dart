class SearchItem {
  final String cardName;
  final String artistName;
  final String imgPath;
  final String tag;
  final bool isNewOffer;
  final String price;

  SearchItem ({
    required this.cardName,
    required this.artistName,
    required this.imgPath,
    required this.tag,
    required this.isNewOffer,
    required this.price,
  });
}
